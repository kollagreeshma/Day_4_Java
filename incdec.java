import java.util.*;
public class incdec{
    public static void main(String args[]){
        int n=5;
        System.out.println(n++);
        int a=4;
        System.out.println(++a);
        int b=6;
        System.out.println(b--);
        int c=3;
        System.out.println(--c);
    }
}